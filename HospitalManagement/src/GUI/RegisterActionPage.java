    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String selectedRole = (String) ComboBox1.getSelectedItem();

        if ("Admin".equals(selectedRole)) {
            new AdminRegister().setVisible(true);
            this.dispose();
        }else if("Doctor".equals(selectedRole)){
            new DoctorRegister().setVisible(true);
            this.dispose();
        }else if("Nurse".equals(selectedRole)){
            new NurseRegister().setVisible(true);
            this.dispose();
        }else if("Patient".equals(selectedRole)){
            new PatientRegister().setVisible(true);
            this.dispose();
        }else if("Pharmacist".equals(selectedRole)){
            new PharmacistRegister().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

